package com.example.companytask;

import java.util.Map;

public class CompanyTask {
	private int taskId;
	private Map<String, Object> params;
	
	public static final int SEATSINFO=1;//输液列表
	public static final int INFUSIONORDERSINFO=2;//获取个人基本信息
	public static final int GETSTART=3;//获取输液信息
	public static final int DRIPSPEED=4;//滴数 
	public static final int TUBES=5;//管型 
	public static final int INFUSIONSTART=6;//开始输液
	public static final int INFUSIONRESTART=7;//继续
	public static final int INFUSIONPAUSE=8;//停止当前输液 
	public static final int INFUSIONFINISH=9;//完成当前输液
	
	public CompanyTask(int taskId,Map<String, Object> params) {
		super();
		this.taskId = taskId;
		this.params=params;
	}

	public int getTaskId() {
		return taskId;
	}

	public void setTaskId(int taskId) {
		this.taskId = taskId;
	}

	public Map<String, Object> getParams() {
		return params;
	}

	public void setParams(Map<String, Object> params) {
		this.params = params;
	}
}
